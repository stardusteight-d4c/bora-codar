export const loadingPrimary = (category: string, variant: string) => {
  return category === 'loading' && variant === 'primary'
}

export const loadingSecondary = (category: string, variant: string) => {
  return category === 'loading' && variant === 'secondary'
}

export const loadingTertiary = (category: string, variant: string) => {
  return category === 'loading' && variant === 'tertiary'
}
