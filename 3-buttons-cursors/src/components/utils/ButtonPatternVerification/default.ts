export const defaultPrimary = (category: string, variant: string) => {
  return category === 'default' && variant === 'primary'
}

export const defaultSecondary = (category: string, variant: string) => {
  return category === 'default' && variant === 'secondary'
}

export const defaultTertiary = (category: string, variant: string) => {
  return category === 'default' && variant === 'tertiary'
}
