export const hoverPrimary = (category: string, variant: string) => {
  return category === 'hover' && variant === 'primary'
}

export const hoverSecondary = (category: string, variant: string) => {
  return category === 'hover' && variant === 'secondary'
}

export const hoverTertiary = (category: string, variant: string) => {
  return category === 'hover' && variant === 'tertiary'
}
