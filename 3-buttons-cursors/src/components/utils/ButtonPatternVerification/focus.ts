export const focusPrimary = (category: string, variant: string) => {
  return category === 'focus' && variant === 'primary'
}

export const focusSecondary = (category: string, variant: string) => {
  return category === 'focus' && variant === 'secondary'
}

export const focusTertiary = (category: string, variant: string) => {
  return category === 'focus' && variant === 'tertiary'
}
