export const movablePrimary = (category: string, variant: string) => {
  return category === 'movable' && variant === 'primary'
}

export const movableSecondary = (category: string, variant: string) => {
  return category === 'movable' && variant === 'secondary'
}

export const movableTertiary = (category: string, variant: string) => {
  return category === 'movable' && variant === 'tertiary'
}
